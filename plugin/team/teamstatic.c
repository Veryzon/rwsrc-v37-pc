RpAtomic *
_rpTeamStaticAttachCustomPipeline(RpAtomic *atomic)
{
    RxPipeline *pipeline;

    RWFUNCTION(RWSTRING("_rpTeamStaticAttachCustomPipeline"));
    RWASSERT(NULL != atomic);

    pipeline = _rpTeamStaticGetCustomPipeline();
    RWASSERT(NULL != pipeline);

    /* Need to attach the custom pipeline. */
    atomic = RpAtomicSetPipeline(atomic, pipeline);
    RWASSERT(NULL != atomic);

    RWRETURN(atomic);
}

