static void
rpPTankD3D9InsPosNCCSNR_spr(RpPTankLockStruct *dstCluster,
                        RwV3d *right,
                        RwV3d *up,
                        RwInt32 pCount,
                        RpPTankData *ptankGlobal)
                        /* no center, constant size, no rotation */
{
    RwInt32 i;
    RwV3d *src;
    RwV3d *dst;
    RwUInt8 *uSrc;
    RwUInt32 uStride;
    RwUInt8 *uDst;
    RwUInt32 uDStride;

    RWFUNCTION(RWSTRING("rpPTankD3D9InsPosNCCSNR_spr"));
    RWASSERT(TRUE == rpTankD3D9UsePointSprites);

    uSrc = ptankGlobal->clusters[RPPTANKSIZEPOSITION].data;
    uStride = ptankGlobal->clusters[RPPTANKSIZEPOSITION].stride;

    uDst = dstCluster->data;
    uDStride = dstCluster->stride;

    if (uStride == uDStride &&
        uStride == sizeof(RwV3d))
    {
        memcpy(uDst, uSrc, pCount * sizeof(RwV3d));
    }
    else
    {
        for (i = 0 ; i < pCount ; i++)
        {
            src = (RwV3d*)uSrc;

            /* vtx 0 */
            dst = (RwV3d*)uDst;
            dst->x = src->x;
            dst->y = src->y;
            dst->z = src->z;
            uDst += uDStride;

            uSrc += uStride;

        }
    }

    RWRETURNVOID();
}

