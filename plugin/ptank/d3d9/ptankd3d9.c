/**
 * \ingroup rpptankd3d9
 *
 * \page rpptankd3d9overview D3D9 RpPTank Overview
 *
 * The D3D9 optimized verion of RpPTank uses point sprites when the hardware supports them.
 * Please note that point sprites severely limits the capabilities of the billboards.
 * Due to hardware limitations, only the following flags are respected:
 *
 * \li rpPTANKDFLAGPOSITION
 * \li rpPTANKDFLAGNORMAL
 * \li rpPTANKDFLAGSIZE (if the hardware supports the D3DFVF_PSIZE vertex format flag)
 * \li rpPTANKDFLAGCOLOR
 * \li rpPTANKDFLAGUSECENTER
 * \li rpPTANKDFLAGCNSVTX2TEXCOORDS
 * \li rpPTANKDFLAGARRAY
 * \li rpPTANKDFLAGSTRUCTURE
 *
 * All the other flags are ignored. You can also specify a constant color or a constant
 * size for all the billboards.
 *
 * \note As the D3D9 point sprites are always square, only the x size of the constant size
 * will be taken into account.
 * \note As the texture coordinates are generated by the hardware and can't be specified, 
 * calls to RpPTankAtomicSetConstantVtx2TexCoords are ignored, but the flag is 
 * needed to specify that the point sprites are textured.
 *
 */

