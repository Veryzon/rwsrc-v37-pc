/**
 * \ingroup rpskinnull
 * \ref RpSkinGetNullPipeline
 *
 * Returns one of the RpSkins internal platform specific pipelines.
 *
 * The skin plugin must be attached before using this function.
 *
 * \param nullPipeline Type of the requested pipeline.
 *
 * \return The \ref RxPipeline requested from the plugin, or
 * NULL if the pipeline wasn't constructed.
 *
 * \see RpSkinAtomicSetType
 * \see RpSkinAtomicGetType
 */
RxPipeline *
RpSkinGetNullPipeline( RpSkinNullPipeline nullPipeline )
{
    RxPipeline *pipeline;

    RWAPIFUNCTION(RWSTRING("RpSkinGetNullPipeline"));
    RWASSERT(0 < _rpSkinGlobals.module.numInstances);
    RWASSERT(rpNASKINNULLPIPELINE < nullPipeline);
    RWASSERT(rpSKINNULLPIPELINEMAX > nullPipeline);

    pipeline = _rpSkinPipeline(nullPipeline);

    RWRETURN(pipeline);
}


